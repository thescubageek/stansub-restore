<h1>Deepsea Expeditions</h1>

<h2>Exciting News!</h2>

<div style="float: right; padding-left: 10px;"><img src="images/shark.gif" width="180" height="180" alt="Six-Gilled Shark" border="0" /></div>
<p>Roatan has grown to where a steady supply of pig organs are now available. Therefore, I am now making regular deposits of food at a GPS-marked location to attract the elusive six-gill shark. I'm happy to report that on nearly every night dive there are regular sitings of these massive prehistoric sharks.</p>

<p>The six-gill shark is one of the largest but least known predatory sharks in the world. This rare opportunity has drawn film crews from around the globe, including National Geographic Television, to record this seldom-observed ocean giant from the safety of <a href="../submarine/index.php"><i>Idabel</i></a>.</p>

<p><a href="../contact/index.php" title="contact Karl Stanley">Contact me</a> today to book your expedition!</p>

