<p>Lophelia coral is believed to cover more area than shallow coral reefs; however, since it is found deeper than even technical divers dare venture, few people have ever seen this splendid marine habitat.</p>
<p>A journey on <a href="../submarines/index.php"><i>Idabel</i></a> to the Lophelia Reef is the "grand tour": we go past the six-gill feeding station, a 110-foot-long shipwreck, and, after a mile of sub-sea travel, arrive at an oasis of deep (1200-1500ft/365-460m) sea coral.</p>

