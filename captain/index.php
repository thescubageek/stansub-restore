<p>Captain Karl Stanley has spent over two-thirds of his life pursuing his desire to explore the planet's "final frontier". He has designed and built two deep-diving submersibles which he has piloted in three different countries on more than 1000 dives, with missions ranging from just a few hours up to seventeen hours. At the age of 24, he began taking people on expeditions down the <a href="../location/index.php">Cayman Trench off Roatan</a>.</p>

<p>While many people have spent more time in libraries, labratories, and classrooms studying the ocean, few people have accumulated as many hours directly observing deep sea aquatic life as Karl Stanley. Karl has recorded over 1000 dives ranging from 500 to 2200 feet underwater and each lasting from a few hours up to seventeen hours at a time.</p>

<p>Karl Stanley has a degree in American Studies from Eckerd College. The story of Karl and his submarines has been <a href="../news/index.php">featured in stories</a> by <a href="http://www.nationalgeographic.com/adventure/0211/q_n_a.html">National Geographic</a> and on the Discovery Channel. Karl is a member of the Deep Submersible Pilots Association and the ACLU; he is available for technnical consulting and public speaking.</p>

