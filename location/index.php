  <div id="body_content"><h1>The Location:<br />Roatan, Honduras</h1>

<div id="map" style="width: 588px; height: 392px; border: 1px dashed #fff; background: black;"></div>

<p>Roatan lies about 40 miles (65 kilometers) off the northern coast of Honduras. Measuring approximately 37 miles (60 kilometers) from east to west and less than 4 miles (7 kilometers) across at its widest point, Roatan is the largest of the three main  islands that, along with Guanaja and Utila, comprise the majority of <i>Las  Islas de La Bahia</i> (The Bay Islands). These islands are joined by three  smaller islands to the east (Barbareta, Morat, and Helene) and the sixty-five  tiny keys of <i>Cayos Cochinos</i> (Hog Islands) to the south.</p>

<p>The islands rest atop the underwater Bonacca Ridge, a mountainous culmination of eons of tectonic plate movement stemming from the Cayman Trench to the north. From its highest point near 270m/900ft elevation to the white-sand shores at sea level, the entire island rests atop an exposed ancient coral reef. The Cayman Trench is 25,200 feet (7680 meters) deep at its center; in comparison, the Grand Canyon is less than 6,000 feet (1800 meters) deep. Roatan's fringing coral reef system, which extends directly from the island's shoreline, forms a near-vertical wall for the first 2000 feet (610 meters) down. If the deep water were not so close to shore, the submersible would need a surface support ship for transportation and the operating costs would be prohibitive.</p>

<p>Typical daytime temperatures hover in the mid-80's &deg;F (27-30 &deg;C) while nighttime temperatures stay in the upper 60's &deg;F (19-22 &deg;C).  Most of the year is spent during the dry season where brief refreshing tropical showers periodically interrupt otherwise sunny skies and crystal-clear waters.  Roatan's rainy season extends from mid-November to early February. The weather is more finicky during this time of year: rain and wind can persist for days only to suddenly give way to weeks of pristine sunny conditions.</p>

<p>Coxen Hole, the municipal center and most densely populated  town in Roatan, is your most likely point of ingress to the island, as it is  home to the airport, cruise ship dock, and ferry dock. French Harbor is the island's main center of commerce, though historical Oak Ridge supports a bulk  of the commercial fishing industry. Punta Gorda, Paya Bay, and Santa Helena to the east maintain much of their unique cultural heritage. On the opposite end of Roatan, Sandy Bay, West End and West Bay form the island's tourist hub.</p>
