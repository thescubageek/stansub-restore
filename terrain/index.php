<p>Roatan's fringing reef system rises up from the Cayman Trench atop the Bonacca Ridge. The coral reef extends directly from the shoreline, fringing the perimeter of the island with a near-vertical wall . From 300 feet (90 meters) down to 1,200 feet, depending on the site, can be completely vertical. An example of this profile would be immediately to the east of Half Moon Bay where a dive boat broke up on the reef, and the largest piece of it is now perched on a small ledge at 860 feet. This typically marks the range where falling objects start to come to rest.</p>

<p>Due to Roatan's geological shiftings, this includes enormous (sometimes more than 100 feet high) blocks of fossilized reef that have sheared off the wall. This creates a rich and varied terrain, one alternating with sandy patches, rock faces, caves, and crevices.</p>

<p>The vertical section is also a very interesting area to explore, as sea level raising and dropping 450 feet through the ice ages has left fossilized reef that the raising waters have dissolved into delicate and bizarre formations.</p>

