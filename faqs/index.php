<a name="faq1"></a>
<p class="subtitle">Is it safe?</p>

<p>The <a href="../sub/index.php" title="deep diving submarine">deep diving submersible <i>Idabel</i></a> is not classed or insured due to extreme overhead cost and paperwork involved. I personally got involved with submersibles to explore the oceans, not deal with inspectors, lawyers and insurance agents. I have, however, incorporated most safety systems that any classed and insured submersible would have.  This submarine you will be riding in is the result of over 24 years of dedicated effort on my part to design the best vehicle possible. As of the spring of 2008, the two vessels I have designed and built have made well over 1000 dives. The worst injury anyone suffered during this was scratching their forehead in the process of boarding.</p>
 
<p>Some safety features of <a href="../sub/index.php" title="deep diving submarine"><i>Idabel</i></a> include:</p>
<ul>
<li>Twin high-pressure air systems</li>
<li>350 pound drop weight</li>
<li>3 days of life support</li>
<li>VHF radio and cell phone carried for surface communication</li>
<li>Fully redundant propulsion systems designed so that if any battery bank, motor or switch fails, the sub will make it back to the dock under her own power</li>
</ul>
 
<hr /> 
<a name="faq2"></a> 
<p class="subtitle">How does the submarine work?</p>

<p><a href="../sub/index.php" title="deep diving submarine"><i>Idabel</i></a> goes up and down using the same basic buoyancy principles as nearly all submarines: by adjusting the amount of air or water inside the ballast compartments. On the surface <a href="../sub/index.php" title="deep diving submarine"><i>Idabel</i></a> is over 1000 pounds positively buoyant. The passengers are weighed and lead weight is added inside the sub that so when the ballast tanks are filled with water, the vessel will only sink by about 20 pounds. This is why it takes approximately 40 minutes to sink 2000 feet&mdash; we are just slightly negatively buoyant.</p>
<p>To get neutrally or positively buoyant, a little bit of air from either of the large air tanks is injected into one of the two ballast tanks. Completely filling the tanks with air, dropping the weight, and/or using the vertical motors would rocket the sub to the surface in 2-3 minutes from 2000 feet.</p>
 
<hr />
<a name="faq3"></a>
<p class="subtitle">Can I bring my camera?</p>
 
<p>Absolutely! The sub is designed such that the passengers get the closest view with the best lighting through the main forward dome. All the <a href="../photos/index.php">photos</a> and <a href="../videos/index.php">videos</a> you see on this site were shot through that large window.</p>

<p>As this is not your typical tourist attraction, the standard copyright policies towards photographs do not apply. It is quite possible that you will acquire media of creatures about which so little is know (possibly even an animal never photographed or filmed before). As this evidence may have value for publications such as encyclopedia articles and scientific journals, it is my policy that I retain copyright on any media shot from the sub, even if shot by a passenger on their personal equipment. However, you will never be denied your own photographs or films for your private use.</p>
 
<hr />
<a name="faq4"></a>
<p class="subtitle">What are the tour times?</p>
 
<p>Again, this is not your typical attraction. You are chartering perhaps the most exclusive transportation device around; accordingly, I believe you should be able to do so at any hour of day or night.</p>

<p>Besides your schedule, there are many options to consider when arranging a time for your sub tour. Six-gill sharks are strictly noctural; therefore, if you are interested in seeing these elusive giants, it is best to dive at night. However, during the winter months, winds can often pick up in the afternoon, making the morning the most desirable and safest time of day to embark.  If you are doing a 1000 foot dive I generally advise diving between 9am and noon, as the sun is directly overhead and the views up the wall are amazing.  If you go at night, bioluminescent animals, hatchet fish, bristlemouth fish, and squid are observed; however, the long-range visibility (up to 600 feet vertically, 300 feet horizontally) that you can have in the day is lost.  As in most ecosystems, animal activity is greatest at dawn and dusk.</p>

<hr /> 
<a name="faq5"></a>
<p class="subtitle">What should I wear?</p>
         
<p>To some extent this is a factor of where you are from and how accustomed to cold weather you are. I have been in the sub on a shark dive, shivering, while my passengers from Canada have yet to put on their jacket. For most people though, the following guidelines will apply:</p>

<ul> 
<li><a href="../expeditions/index.php#1000">1000 foot dive</a>: shorts and a t-shirt</li>
<li><a href="../expeditions/index.php#1500">1500 foot dive</a>: a long sleeve shirt</li>
<li><a href="../expeditions/index.php#2000">2000 foot dive</a>: a long sleeve shirt or jacket and socks</li>
<li><a href="../expeditions/index.php#lophelia">Lophelia Reef dive</a>: a long sleeve shirt or jacket, pants, and socks</li>
</ul>
          
<p>A fleece blanket is always available on request. Towels are provided to wipe any condensation from the walls and can be used for additional padding and/or insulation.</p>

<hr /> 
<a name="faq6"></a>
<p class="subtitle">What will I see down there?</p>
 
<p>This can be separated into two categories:</p>
 
<p class="indented"><b><a href="../terrain/index.php" title="Cayman Trench">The terrain</a></b><br />This by itself would be worth the price of admission. Imagine taking a helicopter tour of the Grand Canyon. The animals are not the main attraction but rather the place itself. In <i>Idabel</i> we will descend up to 2000 feet (630m) down the Cayman Trench wall.  The trench continues to over 25,000 feet beneath the ocean surface (in comparison, the Grand Canyon is merely 5,000 feet deep). The area between 250-500 feet has the most bizarre rock formations you will ever see. During past ice ages the sea level was up to 450 feet lower than it is now, allowing the coral reef to flourish down to what is now 700 feet under water. Then when sea levels rose again, deep water currents eroded the soft limestone made by coral animals into many intricate and delicate shapes. The phantasmagoric results of these processes will amaze you.</p>
 
<p class="indented"><b><a href="../life/index.php">The animals</a></b><br />We have yet to find life in space, but are finding new forms of life in the ocean every year. Scientists still struggle to classify the family of animals to which some of these new species belong. On a dive with me, you will come face-to-face with some of these truly alien creatures. Look through the <a href="../photos/index.php">photos</a> on this website to learn more about the inhabitants of this barely-explored realm of our planet.</p>

<hr /> 
<a name="faq7"></a>
<p class="subtitle">Will it make me claustrophobic?</p>
 
      <p>Your fear is a common one but, once underwater, your concerns will become a thing of the past as a whole new world emerges before you. Most people report the experience as being no more uncomfortable than riding on a small commercial airliner, though I personally find the ride to be a lot smoother.
      
     <p>On the best dives, your perception of the sub being a limiting space is completely reversed&mdash for example, upon first sighting of a <a href="../expeditions/index.php#shark">large six-gill shark</a>, the sub suddenly feels like a safe, comforting cocoon!</p> 

<hr />
<a name="faq8"></a> 
<p class="subtitle">How many people can fit in the sub?</p>
 
      <p><a href="../sub/index.php" title="deep diving submarine"><i>Idabel</i></a> is designed for two passengers and a pilot. However, if you want to split the expense amongst more people, you may squeeze as many people into the front compartment as will fit under the 460lb maximum weight limit.
      
      <p>So far, the record number of passengers for a single dive is six.</p>  

<hr /> 
<a name="faq9"></a>
<p class="subtitle">What if the weather forces a cancellation?</p>
 
      <p>I do not believe that people should be penalized for events beyond their control.  If a dive is canceled due to bad weather, missed flights, family emergencies, illness, etc., a full and prompt refund will be issued. However, a %20 fee will be retained in instances of cancellation due to change-of-heart, hangovers, or laziness. At least 24 hours notice is appreciated when canceling when the weather is not involved. </p>

